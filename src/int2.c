#include "ft_math.h"

t_int2	int2_add(t_int2 l, t_int2 r)
{
	return ((t_int2){l.x + r.x, l.y + r.y});
}

t_int2	int2_sub(t_int2 l, t_int2 r)
{
	return ((t_int2){l.x - r.x, l.y - r.y});
}
